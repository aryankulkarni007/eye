use hir::core::{
    Body, Expr, ExprId, Function, HIR, Literal, Pat, Resolution, Stmt, Struct, TypeRef,
};

pub struct CGen<'a> {
    hir: &'a HIR,
    output: String,
    indent_level: usize,
}

impl<'a> CGen<'a> {
    pub fn new(hir: &'a HIR) -> Self {
        Self {
            hir,
            output: String::new(),
            indent_level: 0,
        }
    }

    pub fn gen_all(mut self) -> String {
        self.output
            .push_str("// generated by the Eye Compiler v0.1\n");
        self.output.push_str("#include <stdio.h>\n");
        self.output.push_str("#include <stdint.h>\n");
        self.output.push_str("#include <stdbool.h>\n\n");

        self.hir
            .structs
            .iter()
            .for_each(|(_id, struct_def)| self.gen_struct(struct_def));

        self.hir
            .functions
            .iter()
            .for_each(|(_id, r#fn)| self.gen_function(r#fn));

        self.output
    }

    fn push_indent(&mut self) {
        self.output.push_str(&"\t".repeat(self.indent_level));
    }

    fn map_type_ref(&self, ty: &TypeRef) -> String {
        match ty {
            TypeRef::Path(name) => match name.as_str() {
                "int32" => "int32_t".to_string(),
                "bool" => "bool".to_string(),
                other => other.to_string(),
            },
            TypeRef::Error => "void* /* ERROR TY */".to_string(),
        }
    }

    fn gen_struct(&mut self, struct_def: &Struct) {
        self.output
            .push_str(&format!("typedef struct {} {{\n", struct_def.name));
        self.indent_level += 1;

        for &field_id in &struct_def.fields {
            let field = &self.hir.fields[field_id];
            self.push_indent();
            let ty_str = self.map_type_ref(&field.ty);
            self.output
                .push_str(&format!("{} {};\n", ty_str, field.name));
        }

        self.indent_level -= 1;
        self.output
            .push_str(&format!("}} {};\n\n", struct_def.name));
    }

    fn gen_function(&mut self, r#fn: &Function) {
        let ret_type = if r#fn.name == "main" {
            "int".to_string()
        } else {
            r#fn.ret
                .as_ref()
                .map_or("void".to_string(), |t| self.map_type_ref(t))
        };

        self.output
            .push_str(&format!("{} {}(", ret_type, r#fn.name));

        for (i, param) in r#fn.params.iter().enumerate() {
            if i > 0 {
                self.output.push_str(", ");
            }
            let p_ty = self.map_type_ref(&param.ty);
            self.output.push_str(&format!("{} {}", p_ty, param.name));
        }
        self.output.push_str(") {\n");
        self.indent_level += 1;

        if let Some(body_id) = r#fn.body {
            let body = &self.hir.bodies[body_id];
            self.gen_body(body);
        }

        if r#fn.name == "main" {
            self.push_indent();
            self.output.push_str("return 0;\n");
        }

        self.indent_level -= 1;
        self.output.push_str("}\n\n");
    }

    pub fn gen_body(&mut self, body: &Body) {
        for &stmt_idx in &body.block {
            let stmt = &body.stmts[stmt_idx];
            self.gen_stmt(stmt, body);
        }
    }

    fn gen_stmt(&mut self, stmt: &Stmt, body: &Body) {
        match stmt {
            Stmt::Let {
                pat,
                ty,
                init,
                mutable: _,
            } => {
                self.push_indent();
                let type_str = match ty {
                    Some(t) => self.map_type_ref(t),
                    None => "int32_t".to_string(),
                };
                let pat_node = &body.pats[*pat];
                let local_idx = match pat_node {
                    Pat::Bind(id) => *id,
                    Pat::Missing => return, // skip if syntax is broken
                };
                let var_name = &body.locals[local_idx].name;
                self.output.push_str(&format!("{} {}", type_str, var_name));

                if let Some(expr_idx) = init {
                    self.output.push_str(" = ");
                    self.gen_expr(*expr_idx, body);
                }

                self.output.push_str(";\n");
            }
            Stmt::Expr(expr_idx) => {
                self.push_indent();
                self.gen_expr(*expr_idx, body);
                self.output.push_str(";\n");
            }
        }
    }

    fn gen_expr(&mut self, expr_idx: ExprId, body: &Body) {
        match &body.exprs[expr_idx] {
            Expr::Missing => self.output.push_str("/* MISSING EXPR */"),
            Expr::Literal(literal) => match literal {
                Literal::Int(val) => self.output.push_str(&val.to_string()),
                Literal::Float(val) => self.output.push_str(val.as_str()),
                Literal::String(val) => self.output.push_str(&format!("\"{}\"", val)),
                Literal::Bool(val) => self.output.push_str(if *val { "true" } else { "false" }),
                Literal::Char(val) => self.output.push_str(&format!("'{}'", val)),
            },
            Expr::Path(resolution) => match resolution {
                Resolution::Local(id) => self.output.push_str(&body.locals[*id].name),
                Resolution::Fn(id) => self.output.push_str(&self.hir.functions[*id].name),
                Resolution::Struct(id) => self.output.push_str(&self.hir.structs[*id].name),
                Resolution::Unresolved(name) => self.output.push_str(name.as_str()),
            },
            Expr::StructLit { ty, fields } => {
                let ty_str = self.map_type_ref(ty);
                self.output.push_str(&format!("({}){{ ", ty_str));
                for (i, field) in fields.iter().enumerate() {
                    if i > 0 {
                        self.output.push_str(", ");
                    }
                    self.output.push_str(&format!(".{} = ", field.name));
                    self.gen_expr(field.value, body);
                }
                self.output.push_str(" }");
            }
            Expr::Call { callee, args } => {
                if let Expr::Path(Resolution::Unresolved(name)) = &body.exprs[*callee]
                    && name == "print"
                {
                    self.gen_print(args, body);
                    return;
                }
                self.gen_expr(*callee, body);
                self.output.push('(');
                for (i, arg) in args.iter().enumerate() {
                    if i > 0 {
                        self.output.push_str(", ");
                    }
                    self.gen_expr(*arg, body);
                }
                self.output.push(')');
            }
            Expr::Field { base, name } => {
                self.gen_expr(*base, body);
                self.output.push_str(&format!(".{}", name));
            }
            Expr::Binary { op, lhs, rhs } => {
                self.output.push('(');
                self.gen_expr(*lhs, body);
                self.output.push_str(&format!(" {} ", op));
                self.gen_expr(*rhs, body);
                self.output.push(')');
            }
            Expr::Unary { op, operand } => {
                self.output.push_str(&format!("{}", op));
                self.gen_expr(*operand, body);
            }
            Expr::Block(_) => self.output.push_str("/* BLOCK EXPRS DEFERRED */"),
        }
    }

    fn gen_print(&mut self, args: &[ExprId], body: &Body) {
        self.output.push_str("printf(");
        if let Some(format_str_idx) = args.first() {
            if let Expr::Literal(Literal::String(s)) = &body.exprs[*format_str_idx] {
                // turn {} into %d (assuming ints) and add newline
                let c_format = s.replace("{}", "%d");
                self.output.push_str(&format!("\"{}\\n\"", c_format));
            } else {
                self.gen_expr(*format_str_idx, body);
            }
        }

        for arg in args.iter().skip(1) {
            self.output.push_str(", ");
            self.gen_expr(*arg, body);
        }
        self.output.push(')');
    }
}

#[cfg(test)]
mod tests {
    use super::*;
    use ast::{AstNode, SourceFile};
    use hir::core::lower_source_file;
    use lexer::{Lexer, SourceText};

    fn emit(src: &str) -> String {
        let source = SourceText::new(src.to_string());
        let tokens = Lexer::new(&source).tokenize().tokens;
        let parse = parser::parse(&tokens, &source);
        assert!(parse.errors.is_empty(), "parse errors: {:?}", parse.errors);
        let file = SourceFile::cast(parse.green).expect("root is SourceFile");
        let hir = lower_source_file(file);
        assert!(
            hir.diagnostics.is_empty(),
            "hir diagnostics: {:?}",
            hir.diagnostics
        );
        CGen::new(&hir).gen_all()
    }

    /// Canonical `main.eye`. Pinning the C output cements the v0.1 codegen
    /// behaviour - any incidental change downstream forces a snapshot
    /// review.
    #[test]
    fn main_eye_c_output_snapshot() {
        let src = "\
structure Point {
    int32 x,
    int32 y,
};

main() {
    const int32 x = 0;
    const int32 y = 0;
    var Point p = Point { x, y };

    print(\"{}\", p.x);
    print(\"{}\", p.y);
}
";
        insta::assert_snapshot!(emit(src));
    }

    /// Regression for the call-arg emission bug: the loop used to guard
    /// `gen_expr` behind `if i > 0`, dropping arg 0 for every non-`print`
    /// call. The v0.1 parser has no fn-call-with-args path outside
    /// `print(...)`, so we build the HIR directly and feed it into the
    /// expression generator.
    #[test]
    fn user_fn_call_emits_every_argument_in_order() {
        use hir::core::{
            Body, Expr, Function, Literal, Resolution, Stmt, HIR,
        };

        let mut hir = HIR::default();

        let callee_fn = hir.functions.alloc(Function {
            name: "add".into(),
            params: Vec::new(),
            ret: None,
            body: None,
        });

        let mut body = Body::default();
        let callee = body.exprs.alloc(Expr::Path(Resolution::Fn(callee_fn)));
        let a1 = body.exprs.alloc(Expr::Literal(Literal::Int(1)));
        let a2 = body.exprs.alloc(Expr::Literal(Literal::Int(2)));
        let a3 = body.exprs.alloc(Expr::Literal(Literal::Int(3)));
        let call = body.exprs.alloc(Expr::Call {
            callee,
            args: vec![a1, a2, a3],
        });
        let call_stmt = body.stmts.alloc(Stmt::Expr(call));
        body.block.push(call_stmt);
        let body_id = hir.bodies.alloc(body);

        let main_fn = hir.functions.alloc(Function {
            name: "main".into(),
            params: Vec::new(),
            ret: None,
            body: Some(body_id),
        });
        hir.items.values.insert("main".into(), main_fn);
        hir.items.values.insert("add".into(), callee_fn);

        let c = CGen::new(&hir).gen_all();
        assert!(
            c.contains("add(1, 2, 3)"),
            "expected `add(1, 2, 3)` in output, got:\n{c}"
        );
        assert!(
            !c.contains("add(, "),
            "leading separator should not appear, got:\n{c}"
        );
    }

    /// Regression for nested field access: the HIR previously used
    /// `NameRef::nth(1)`, which returns `None` when the base is itself
    /// a `FieldExpr`. The C output should contain the chained `.y`.
    #[test]
    fn nested_field_access_lowers_correctly() {
        let src = "\
structure Inner {
    int32 y,
};

structure Outer {
    Inner i,
};

main() {
    const Inner i = Inner { y: 42 };
    const Outer o = Outer { i: i };
    print(\"{}\", o.i.y);
}
";
        let c = emit(src);
        assert!(
            c.contains("o.i.y"),
            "expected chained field access `o.i.y` in output, got:\n{c}"
        );
    }
}
