use hir::core::{ExprId, HIR};
use rustc_hash::FxHashMap;
use std::fmt::Write as _;

pub struct CGen<'a> {
    hir: &'a HIR,
    output: String,
    indent_level: usize,
    // Hoisted value-position `match`/`if` temps: maps the expression to the
    // `_matchN`/`_ifN` C variable that holds its value (see `matches::hoist_values`).
    value_temps: FxHashMap<ExprId, String>,
    temp_counter: usize,
}

impl<'a> CGen<'a> {
    pub fn new(hir: &'a HIR) -> Self {
        Self {
            hir,
            output: String::new(),
            indent_level: 0,
            value_temps: FxHashMap::default(),
            temp_counter: 0,
        }
    }

    pub fn gen_all(mut self) -> String {
        self.output
            .push_str("// generated by the Eye Compiler v0.7\n");
        self.output.push_str("#include <stdio.h>\n");
        self.output.push_str("#include <stdint.h>\n");
        self.output.push_str("#include <stddef.h>\n"); // size_t, ptrdiff_t
        self.output.push_str("#include <stdbool.h>\n\n");

        self.hir
            .structs
            .iter()
            .for_each(|(_id, struct_def)| self.gen_struct(struct_def));

        self.hir
            .unions
            .iter()
            .for_each(|(_id, union_def)| self.gen_union(union_def));

        self.hir
            .enums
            .iter()
            .for_each(|(_id, enum_def)| self.gen_enum(enum_def));

        // Fixed-array value wrappers. After the nominal types (an array of a
        // struct needs the struct complete) and before any function uses them.
        self.gen_array_typedefs();

        // Extern prototypes come before every definition so a call site sees
        // the C signature regardless of where the `extern` block sits in the
        // source. Bodies are emitted in a second pass.
        self.hir
            .functions
            .iter()
            .filter(|(_id, r#fn)| r#fn.is_extern)
            .for_each(|(_id, r#fn)| self.gen_function(r#fn));

        self.hir
            .functions
            .iter()
            .filter(|(_id, r#fn)| !r#fn.is_extern)
            .for_each(|(_id, r#fn)| self.gen_function(r#fn));

        self.output
    }

    fn push_indent(&mut self) {
        for _ in 0..self.indent_level {
            self.output.push('\t');
        }
    }

    fn write_fmt(&mut self, args: std::fmt::Arguments<'_>) {
        self.output
            .write_fmt(args)
            .expect("writing to String cannot fail");
    }

    fn comma_sep<T>(
        &mut self,
        iter: impl IntoIterator<Item = T>,
        mut emit_item: impl FnMut(&mut Self, T),
    ) {
        for (i, item) in iter.into_iter().enumerate() {
            if i > 0 {
                self.output.push_str(", ");
            }
            emit_item(self, item);
        }
    }
}
